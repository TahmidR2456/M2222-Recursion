func factorialRecursion(num: Int) -> Int {
    print("Calculating factorial(\(num))...")
    if (num <= 1) {
        print("Terminal case, returning 1.")
        return 1
    } else {
        let results  = num * factorialRecursion(num: num-1)
        print("Non-terminal case, returning \(results).")
        return results
    }
}

print("The final result is: \(factorialRecursion(num: n)).")



